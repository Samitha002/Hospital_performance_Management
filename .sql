CREATE TABLE Patients (
    PatientID VARCHAR(10) PRIMARY KEY,
    Name VARCHAR(100),
    Age INT,
    Gender VARCHAR(10),
    InsuranceType VARCHAR(50)
);
CREATE TABLE Admissions (
    AdmissionID VARCHAR(10) PRIMARY KEY,
    PatientID VARCHAR(10),
    Department VARCHAR(50),
    AdmissionDate TIMESTAMP,
    DischargeDate TIMESTAMP,
    AdmissionType VARCHAR(20),
    FOREIGN KEY (PatientID) REFERENCES Patients(PatientID)
);
CREATE TABLE DoctorSchedule (
    DoctorID VARCHAR(10) PRIMARY KEY,
    Name VARCHAR(100),
    Department VARCHAR(50),
    ShiftStart TIME,
    ShiftEnd TIME,
    BookedHours INT
);
CREATE TABLE Procedures (
    ProcedureID VARCHAR(10) PRIMARY KEY,
    AdmissionID VARCHAR(10),
    ProcedureType VARCHAR(100),
    ProcedureDate TIMESTAMP,
    FOREIGN KEY (AdmissionID) REFERENCES Admissions(AdmissionID)
);
CREATE TABLE Billing (
    BillID VARCHAR(10) PRIMARY KEY,
    AdmissionID VARCHAR(10),
    TotalCost DECIMAL(10,2),
    ICUUsed BOOLEAN,
    VentilatorUsed BOOLEAN,
    FOREIGN KEY (AdmissionID) REFERENCES Admissions(AdmissionID)
);
CREATE TABLE Outcomes (
    OutcomeID VARCHAR(10) PRIMARY KEY,
    AdmissionID VARCHAR(10),
    Outcome VARCHAR(20),
    FOREIGN KEY (AdmissionID) REFERENCES Admissions(AdmissionID)
);
 
